#include<stdio.h>
#include<string.h>
#include<ctype.h>
int main()
{
	int num[500]={0},op[500]={0},ans=0,i,j,k,a=0;
	char ch_[500],ch[500];
	gets(ch_);
	for(i=0,j=-1;i<strlen(ch_);i++)
	{
		if(isspace(ch_[i]))
		{
			continue;
		}
		else
		{
			j++;
			ch[j]=ch_[i];
		}
	}
	if(ch[0]=='-')
	{
		op[0]=2;
	}
	else
	{
		op[0]=1;
	}
	for(i=0,j=0,k=1;i<strlen(ch);i++)   //i用来控制ch[], j控制num[],k控制op[] 
	{
		if(isdigit(ch[i]))
		{
			num[j]=num[j]*10+ch[i]-'0';
		}
		else
		{
			if(ch[i]=='+'||ch[i]=='-')
			{
				j++;
				if(ch[i]=='+')
				{
					op[k]=1;
				}
				else
				{
					op[k]=2;
				}
				k++;
				continue;
			}
			else if(ch[i]=='*'||ch[i]=='/')
			{
				if(ch[i]=='*')
				{
					for(a=0,i++;;i++)
					{
						if(isdigit(ch[i]))
							a=a*10+ch[i]-'0';
						else
							{
							i--;
							break;
							}
					}
					num[j]*=a;	
				}
				else if(ch[i]=='/')
				{
					for(a=0,i++;;i++)
					{
						if(isdigit(ch[i]))
							a=a*10+ch[i]-'0';
						else
							{
							i--;
							break;
							}
					}
					num[j]/=a;
				}
				
			}
		}
	}
	for(i=0,ans=0;i<=j;i++)
	{
		if(op[i]==1)
		{
			ans+=num[i];
		}
		else
		{
			ans-=num[i];
		}
	}
	printf("%d",ans);
	return 0;
}

