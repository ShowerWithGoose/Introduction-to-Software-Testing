#include<stdio.h>
int main()
{
    int c,i=0,j,b[200],k1=0,k2=0,x,sum;
    char a[200],d[200];
    while((c=getchar())!='=')
    {
        if(c==' ')
            continue;
        if(c!=' ')
        {
            a[i]=c;
            i++;
        }
    }
    for(j=0;j<i;j++)
    {
        if(a[j]>='0'&&a[j]<='9')
        {
            x=j+1;
            b[k1]=a[j]-'0';
            for(;a[x]>='0'&&a[x]<='9';x++)
            {
                b[k1]=b[k1]*10+a[x]-'0';
            }
            k1++;
            j=x-1;
            continue;
        }
        else
        {
            d[k2]=a[j];
            k2++;
        }
    }
    for(i=0;i<k2;i++)
    {
        if(d[i]=='+'||d[i]=='-')
        {
            continue;
        }
        if(d[i]=='*')
        {
            b[i]=b[i]*b[i+1];
            for(j=i+1;j<k1-1;j++)
            {
                b[j]=b[j+1];
            }
            k1--;
            for(j=i;j<k2-1;j++)
            {
                d[j]=d[j+1];
            }
            k2--;
            i--;
        }
        if(d[i]=='/')
        {
            b[i]=b[i]/b[i+1];
            for(j=i+1;j<k1-1;j++)
            {
                b[j]=b[j+1];
            }
            k1--;
            for(j=i;j<k2-1;j++)
            {
                d[j]=d[j+1];
            }
            k2--;
            i--;
        }
    }
    for(i=0,sum=b[0];i<k2;i++)
    {
        if(d[i]=='+')
            sum+=b[i+1];
        if(d[i]=='-')
            sum-=b[i+1];
    }
    printf("%d",sum);
    return 0;
}

