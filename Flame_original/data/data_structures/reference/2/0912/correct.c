#include<stdio.h>
#include<math.h>
#include<string.h>
int main()
{
	int a[1000],i=0,j,m,t=0,s=0,k=0,c[100];
	int judge=0,n;
	char f[1000],b[1000],d[1000];
	while(1)
	{
		while(1)
		{
			scanf("%c",&f[judge]);
			if(f[judge]!=' ')
			{
				n=0;
				while(1)
				{
					if(f[judge]>='0'&&f[judge]<='9')
					n=n*10+f[judge]-'0';
					if(f[judge]==' '||f[judge]=='*'||f[judge]=='/'||f[judge]=='+'||f[judge]=='-'||f[judge]=='=')
					break;
					judge++;
					scanf("%c",&f[judge]);
				}
				a[i]=n;
				break;
			}
			judge++;
		}
		while(1)
		{
			if(f[judge]!=' ')
			{
			b[i]=f[judge];
			break;
			}
			judge++;
			scanf("%c",&f[judge]);
		}
		judge++;
		if(b[i]=='=')
		break;
		i++;
	}
	if(b[0]=='=')
	{
		printf("%d",a[0]);
	}
	else
	{
		for(j=0;j<=i;j++)
		{
			if(b[j]=='*')
			{
				a[j+1]=a[j]*a[j+1];
				a[j]=-1;
			}
			if(b[j]=='/')
			{
				a[j+1]=a[j]/a[j+1];
				a[j]=-1;
			}
		}
		for(m=0;m<=i;m++)
		{
			if(b[m]=='+'||b[m]=='-')
			{
				d[t]=b[m];
				t++;
			}
			if(a[m]>=0)
			{
				c[s]=a[m];
				s++;
			}
		}
		for(k=0;k<t;k++)
		{
			if(d[k]=='+')
			{
				c[k+1]=c[k]+c[k+1];
			}
			if(d[k]=='-')
			{
				c[k+1]=c[k]-c[k+1];
			}
		}
		printf("%d",c[t]);
	}
} 

