#include<stdio.h>
#define MAXNUM 3 // @@ [The stack maximum capacity should be 100, not 3, as per problem description.]
#define MINNUM 0
int warehouse[MAXNUM];
int num=MINNUM;
int check();

int main()
{
    int op;
    while(1)
    {
        scanf("%d",&op);
        if(op==1)
        {
            int n;
            scanf("%d",&n);
            if(check()==-1)
            {
                printf("error ");
            }
            else
            {
                warehouse[num]=n;
                num++;
            }
        }
        if(op==0)
        {
            int n;
            n=warehouse[num-1]; // @@ [Array index out-of-bounds when stack is empty (num==0). Should check emptiness before accessing warehouse[num-1].]
            if(check()==0)
            {
                printf("error ");
            }
            else
            {
                printf("%d ",n);
                num--;
            }
        }
        if(op==-1)
        {
            break;
        }
    }
    return 0;
}

int check()
{
    if(MINNUM==num)
        return 0;
    if(MAXNUM==num)
        return -1;
    else
        return 1;
}