int main()
{
  int i=0,j=0;
  char s[512],s2[512];//两个字符串数组，一个放原来的，一个放补充的 
  gets(s);
  for(i=0;s[i]!='\0';i++){
    //把s[]传给s2[] 
    s2[j]=s[i];
    if(s[i+1]=='-'&&s[i]<s[i+2]&&((s[i]>='a'&&s[i]-1<'z'&&s[i+2]>'a'&&s[i+2]<='z')||(s[i]>='A'&&s[i]<'Z'&&s[i+2]>'A'&&s[i+2]<='Z') ||(s[i]>='0'&&s[i]<'9'&&s[i+2]>'0'&&s[i+2]<='9'))){  // @@ 条件判断错误：1. s[i]-1<'z' 应为 s[i]<='z'；2. s[i+2]>'a' 应为 s[i+2]>='a'；3. 数字部分类似，s[i]<'9' 应为 s[i]<='8' 或更严谨；4. 缺少类型相同且递增的必要性，导致无法正确识别所有有效展开。
    //延长字符串
    char temp;
    temp=s[i];//把‘-’前的字符串赋给过渡值 
    while(temp<s[i+2]){  // @@ while 循环条件错误：应为 while(temp <= s[i+2])，否则会漏掉右边的字符（如 a-c 中缺少 c），导致展开不完整。
      temp++;
      j++;
      s2[j]=temp;
      }
      i=i+2;  // @@ 索引跳跃过大：应改为 i=i+1 并调整输出逻辑，否则会跳过右边字符（如 a-c-g 中 c 被跳过），导致后续连续展开无法被触发。
    }
    j++; 
    
  } 
  s2[j]='\0';
  puts(s2);
  
    return 0;
}