#define N (int)1e6+1
int main(){
    char str[N];
    gets(str);
    int len=strlen(str);
    for(int i=0;i<len;i++){
        if(str[i]=='-'){
            if('0'<=str[i-1]&&str[i-1]<=str[i+1]&&str[i+1]<='9'){ // @@ Condition does not ensure both characters are digits; allows non-digit on left because '0'<= is always true; uses <= instead of <; missing check for str[i-1]<='9' and str[i+1]>='0'.
                for(int j=str[i-1]+1,k=str[i+1];j<k;j++){
                    printf("%c",j);
                }
            }
            else if('a'<=str[i-1]&&str[i-1]<=str[i+1]&&str[i+1]<='z'){ // @@ Similar: does not ensure both are lowercase letters; allows left to be non-lowercase; uses <= instead of <.
                for(int j=str[i-1]+1,k=str[i+1];j<k;j++){
                    printf("%c",j);
                }
            }
            else if('A'<=str[i-1]&&str[i-1]<=str[i+1]&&str[i+1]<='Z'){ // @@ Similar: does not ensure both are uppercase letters; allows left to be non-uppercase; uses <= instead of <.
                for(int j=str[i-1]+1,k=str[i+1];j<k;j++){
                    printf("%c",j);
                }
            }
            else{
                printf("%c%c%c",str[i-1],str[i],str[i+1]); // @@ Should only print the hyphen (str[i]) because the left and right characters are printed in their own iterations; printing all three causes duplicates.
            }
        }
        else{
            printf("%c",str[i]);
        }
    }
    return 0;
}