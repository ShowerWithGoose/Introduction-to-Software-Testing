#include<stdio.h>
#include<string.h>
int main()
{
    char a[100],b[100],d[100],e[100];
    int i,j=0,ans,m=0,k,n=0;
    int c[100]={0};
    gets(e);
    for(i=0;i<strlen(e);i++){
        if(e[i]!=' '){
            a[j]=e[i];
            j++;
            
        }
    }
    
    j=0;
    
    
    for(i=0;i<strlen(a);i++){
        if(a[i]<='9'&&a[i]>='0'){
            b[j]=a[i];
            
            j++;
        }//连续的数字读入 
        else if(a[i]>'9'||a[i]<'0'){//读到符号后停止 
            for(k=0;k<j;k++){
                c[m]=c[m]*10+b[k]-48;
            }
            
            m++;
            j=0;
        }//c[0]代表了第一个数，以此类推 m
    }
    ans=c[0];
    
    for(i=0;i<strlen(a);i++){
        if(a[i]=='+'||a[i]=='-'||a[i]=='*'||a[i]=='/'||a[i]=='='){
            d[n]=a[i];
            n++;
        }//将符号剔除出来 d[n]
    }
    double x=0;
    for(i=0;i<m;i++){
        if((d[i]=='+'||d[i]=='-')&&d[i+1]!='*'&&d[i+1]!='/'){ // @@ [错误：这个条件错误地处理了加减号后的乘除运算。例如，在表达式 "24/4/3/2*2*3*4" 中，第一个运算符是 '/'，但这里只处理加减号，导致乘除运算被忽略或错误处理。实际上，应该先处理所有乘除运算，再处理加减运算，或者使用正确的优先级逻辑。]
            
            if(d[i]=='+'){
                ans+=c[i+1];
            }
            else if(d[i]=='-'){
                ans-=c[i+1];
            }
            
        }
        else  if(d[i]=='*'||d[i]=='/'){ // @@ [错误：这个分支试图处理连续的乘除运算，但逻辑有缺陷。它只处理了从当前乘除号开始的连续乘除，但没有正确更新数组索引和结果。例如，对于 "24/4/3/2*2*3*4"，它错误地将所有乘除运算合并计算，导致结果144，而不是正确的24。此外，变量x是double类型，但整数除法应使用整数，且最终结果ans是整数，类型不匹配可能导致精度问题。]
            if(d[i]=='*'){
                x=c[i]*c[i+1];
            }
            else if(d[i]=='/'){
                x=c[i]/c[i+1];
            }
            for(j=i+1;j<m;j++){
                if(d[j]=='*'){
                    x*=c[j+1];
                }
                else if(d[j]=='/'){
                    x/=c[j+1];
                }
                else {
                    if(i>=1){
                    if(d[i-1]=='-'){
                        ans-=x;
                    }
                    else {
                        ans+=x;
                    }
                    }
                    else if(i==0){
                        ans=x;
                    }
                    break;
                }
            }
        }
    }
    
    printf("%d",ans);
}