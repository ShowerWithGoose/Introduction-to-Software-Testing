#include<stdio.h>
#include<string.h>
#include<math.h>
#include<stdlib.h>
struct line{
    int x1;
    int y1;
    int x2;
    int y2;
    int num;
};
typedef struct line lin;
lin str[105];
int main(void){
    int i,j,n,max = 1,flag = 0;
    scanf("%d",&n);
    for(i = 0;i < n;i++)
    {
        scanf("%d%d%d%d",&str[i].x1,&str[i].y1,&str[i].x2,&str[i].y2);
        str[i].num = 1;
    }
    for(i = 0;i < n;i++)
    {
        for(j = 0;j < n;j++)
        {
            if(i != j) 
            {
                if(str[i].x2 == str[j].x1&&str[i].y2 == str[j].y1)
                {
                    str[i].x2 = str[j].x2;        // @@ Incorrectly modifies the endpoint of the current segment; prevents proper backward propagation of chain length
                    str[i].y2 = str[j].y2;        // @@ Same as above
                    str[i].num += str[j].num;    // @@ Should add str[i].num to str[j].num instead, and not modify str[i]'s endpoint
                    j = 0;                       // @@ Resetting j can cause re‑scanning but does not fix the core propagation issue; may lead to inefficiency or infinite loops
                }
            }
        }
        if(str[i].num > max)
        {
            max = str[i].num;
            flag = i;
        }
    }
    printf("%d %d %d",max,str[flag].x1,str[flag].y1);
    return 0;
}