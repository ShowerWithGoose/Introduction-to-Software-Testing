#include <stdio.h>
struct xianduan{
    int x1;int y1;
    int x2;int y2;
};
struct xianduan line[101]; 
int main()
{
    int n;
    int j;
    int max=0;////最大线段数
    int lianxu[101];
    int xbegin=0,ybegin=0;    
    scanf("%d",&n);
    int i;
    for(i=0;i<n;i++)
    {
        lianxu[i]=1;/////该线段及以上连续的线段 
    }
    for(i=0;i<n;i++)
    {
       scanf("%d%d%d%d",&line[i].x1,&line[i].y1,&line[i].x2,&line[i].y2);        
    }

    for(i=1;i<n;i++)  // @@ [The algorithm does not sort segments by x1, so the order of processing does not follow the increasing x order required by the problem. This can cause connections to be missed or incorrectly merged.]
    {
      for(j=i-1;j>=0;j--)
      {                
        if(lianxu[i]==0) continue;
        if((line[i].x1==line[j].x1)&&(line[i].y1==line[j].y1))
        {
            line[i].x1=line[j].x2;  // @@ [Modifying line[i] endpoints destroys the original endpoint, which may be needed for future connections. Additionally, this can cause the segment's x1 to become greater than x2, violating the increasing x order assumption.]
            line[i].y1=line[j].y2;
            lianxu[i]=lianxu[i]+lianxu[j];
            lianxu[j]=0;            
        }
        else if((line[i].x1==line[j].x2)&&line[i].y1==line[j].y2)
        {
            line[i].x1=line[j].x1;  // @@ [Same issue: modifying line[i].x1 overwrites the original left endpoint, which may have been the true starting point of the chain.]
            line[i].y1=line[j].y1;
            lianxu[i]=lianxu[i]+lianxu[j];
            lianxu[j]=0;            
        }
        else if((line[i].x2==line[j].x1)&&line[i].y2==line[j].y1)
        {
            line[i].x2=line[j].x2;  // @@ [Modifying line[i].x2 similarly destroys the original right endpoint, potentially losing connection points for later segments.]
            line[i].y2=line[j].y2;
            lianxu[i]=lianxu[i]+lianxu[j];
            lianxu[j]=0;
        }
        else if((line[i].x2==line[j].x2)&&line[i].y2==line[j].y2)
        {
            line[i].x2=line[j].x1;  // @@ [Same destructive modification; also may cause x2 < x1, which is inconsistent with the problem's increasing x condition.]
            line[i].y2=line[j].y1;
            lianxu[i]=lianxu[i]+lianxu[j];
            lianxu[j]=0;
        }                
      }    
    }
    int start=0;    
    for(i=0;i<n;i++)
    {
        if(lianxu[i]>max)
        {
            max=lianxu[i];
            start=i;
        }
    }
    if(line[start].x1<line[start].x2)  // @@ [This comparison only checks the two endpoints of the final merged segment, but due to destructive merging, the segment may not represent the extreme points of the true chain. The starting point should be the leftmost endpoint of the entire chain, which may belong to a segment that was merged away.]
    {
        xbegin=line[start].x1;
        ybegin=line[start].y1;
    }
    else if(line[start].x2<line[start].x1)
    {
        xbegin=line[start].x2;
        ybegin=line[start].y2;        
    }

    printf("%d %d %d",max,xbegin,ybegin);
    return 0;
}