#include<stdio.h>
#include<string.h>
char s[1000]={0};
int main()
{
	int m=0,k=0,count;
	gets(s);
	int len=strlen(s);
	for(k=0;k<len;k++)
	{
		if(s[k]=='.')
		{
			m=k;
		}
	}
	if(s[0]=='-')
	{
		if(m==2&&s[1]!='0')
	    {
	    	printf("-");
		    for(k=1;k<len;k++)
		    {
			    printf("%c",s[k]);
		    }
	     	printf("e0");
	    }
	    else if(m==2&&s[1]=='0')
	    {
	    	printf("-");
		    for(k=3;k<len;k++)
		    {
			    if(s[k]!='0')
			    {
				    count=k;
				    break;
			    }
		    }
		    printf("%c",s[count]);
		    if(count==len-1)
		    {
			    printf("e");
			    printf("%d",2-count);
		    }
		    else
		    {
			    printf(".");
			    for(k=count+1;k<len;k++)
		        {
			        printf("%c",s[k]);	
		        }
		        printf("e");
		        printf("%d",2-count);
		    }
        }
	    else
	    {
	    	printf("-");
		    printf("%c",s[1]);
		    printf(".");
		    for(k=1;k<m;k++)
		    {
			    printf("%c",s[k]);
		    }
		    for(k=m+1;k<len;k++)
		    {
			    printf("%c",s[k]);
		    }
		    printf("e");
		    printf("%d",m-2);
	    }
	}
	else
	{
		if(m==1&&s[0]!='0')
	    {
		    for(k=0;k<len;k++)
		    {
			    printf("%c",s[k]);
		    }
	     	printf("e0");
	    }
	    else if(m==1&&s[0]=='0')
	    {
		    for(k=2;k<len;k++)
		    {
			    if(s[k]!='0')
			    {
				    count=k;
				    break;
			    }
		    }
		    printf("%c",s[count]);
		    if(count==len-1)
		    {
			    printf("e");
			    printf("%d",1-count);
		    }
		    else
		    {
			    printf(".");
			    for(k=count+1;k<len;k++)
		        {
			        printf("%c",s[k]);	
		        }
		        printf("e");
		        printf("%d",1-count);
		    }
        }
	    else
	    {
		    printf("%c",s[0]);
		    printf(".");
		    for(k=1;k<m;k++)
		    {
			    printf("%c",s[k]);
		    }
		    for(k=m+1;k<len;k++)
		    {
			    printf("%c",s[k]);
		    }
		    printf("e");
		    printf("%d",m-1);
	    }
	}
	
	return 0;
}

