#include<stdio.h>
#include<string.h>
int main()
{
    int i,j=0,m=0,n=0,r,q=0,sum,p=0,k=0;
    int a[100],d[100];
    char s[100],str[100],b[100],c[100];
    gets(s);
    for(i=0;i<strlen(s);i++)
    {
        if(s[i]!=' ')
        {
            str[m]=s[i];
            m++;
        }
    }
    for(i=0;i<m;i++)
    {
        for(a[n]=0;str[i]>='0'&&str[i]<='9';i++)
        {
            a[n]=10*a[n]+str[i]-'0';
        }
        n++;
        if(str[i]<'0')
        {
            b[j]=str[i];
            j++;
            if(str[i]=='+'||str[i]=='-')
            {
                c[k]=str[i];
                k++;
            }
        }
    }
    if(j==0)
    {
        printf("%d",a[0]);
    }
    else
    {
        for(i=0;i<strlen(b);i++)
        {
            if(b[i]=='+'||b[i]=='-')
            {
                if(i==0)
                {
                    d[p]=a[0];
                    p++;
                    if(b[i+1]=='+'||b[i+1]=='-')
                    {
                        d[p]=a[i+1];
                        p++;
                    }
                }
                else
                {
                    if(b[i+1]=='+'||b[i+1]=='-'||i==strlen(b)-1)
                    {
                        d[p]=a[i+1];
                        p++;
                    }
                }
            }
            if(b[i]=='*'||b[i]=='/')
            {
                if(b[i]=='*')
                    d[p]=a[i]*a[i+1];
                if(b[i]=='/')
                    d[p]=a[i]/a[i+1];
                for(r=i+1;r<strlen(b);r++)
                {
                    if(b[r]=='*')
                    {
                        q++;
                        d[p]=d[p]*a[r+1];
                    }
                    else if(b[r]=='/')
                    {
                        q++;
                        d[p]/=a[r+1];
                    }
                    else
                    {
                        break;
                    }
                }
                p++,i+=q,q=0;
            }
        }
        sum=d[0];
        for(i=0;i<strlen(c);i++)
        {
            if(c[i]=='+')
            {
                sum=sum+d[i+1];
            }
            if(c[i]=='-')
            {
                sum=sum-d[i+1];
            }
        }
        printf("%d",sum);
    }
    return 0;
}

