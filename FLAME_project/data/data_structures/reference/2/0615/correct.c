#include<stdio.h>
#include<string.h>
char s[10000]={0};
int link[10000]={0};
int num[10000]={0};
int main()
{
	int ans=0,i=0,j=0,k=0,tmp; 
	gets(s);
	for(i=0;i<strlen(s);i++)
	{
		if(s[i]>='0'&&s[i]<='9')
		{
			num[j]*=10;
			num[j]+=(s[i]-'0');
		}
		if(s[i]=='-'||s[i]=='+'||s[i]=='*'||s[i]=='/')
		{
			j++;
		}
	}
	for(i=0;i<strlen(s);i++)
	{
		if(s[i]=='+')
		{
		    link[k]++;
		    k++;
		}
		if(s[i]=='-')
		{
		    link[k]+=2;
		    k++;
		}
		if(s[i]=='*')
		{
		    link[k]+=3;
		    k++;
		}
		if(s[i]=='/')
		{
		    link[k]+=4;
		    k++;
		}
	}
	ans=num[0];
	j=1;
	for(i=0;i<k;i++)
	{
		if(link[i]==3)
		{
			ans*=num[j];
			j++;
		}
		else if(link[i]==4)
		{
			ans/=num[j];
		    j++;
		}
		else
		{
			if(link[i+1]==1||link[i+1]==2||link[i+1]==0)
			{
				if(link[i]==1)
				ans+=num[j];
				if(link[i]==2)
				ans-=num[j];
				j++;
			}
			else
			{
				for(tmp=i;link[tmp+1]==3||link[tmp+1]==4;tmp++)
				{
					if(link[tmp+1]==3)
					{
					    num[j]*=num[j+1];
					    num[j+1]=num[j];
					    j++;
					}
					if(link[tmp+1]==4)
					{
						num[j]/=num[j+1];
						num[j+1]=num[j];
						j++;
					}
				}
				if(link[i]==1)
				ans+=num[j];
				if(link[i]==2)
				ans-=num[j];
				i=tmp;
				j++;
			}
		}
	}
	printf("%d",ans);
	return 0; 
} 

