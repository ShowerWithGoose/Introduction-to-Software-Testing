#include<stdio.h>
#include<string.h>
int main()
{
	int len,k,i,j;
	char s[2000];
	char n[2000];
	gets(n);
	len=strlen(n);
	for(i=0;i<len;i++)
	{
		if(n[i]=='.')
		{
			k=i;
		}
	}
	if(k!=1)
	{
		s[0]=n[0];
		s[1]='.';
		for(i=1,j=2;i<len;i++)
		{
			if(n[i]!='.')
			{
				s[j]=n[i];
				j++;
			}
		}
		for(i=0;i<len;i++)
		{
			printf("%c",s[i]);
	
		}
		printf("e%d",k-1);
	}
	else
	{
		if(n[0]!='0')
		{
			for(i=0;i<len;i++)
			{
				printf("%c",n[i]);
			}
			printf("e0");
		}
		else
		{
			for(i=2;i<len;i++)
			{
				if(n[i]!='0')
				{
					j=i;
					break;
				}
			}
			if(j==(len-1))
			{
				for(i=j;i<len;i++)
				{
					printf("%c",n[i]);
				}
				printf("e-%d",j-1);
			}
			else
			{
				n[0]=n[j];
				n[1]='.';
				for(i=0;i<2;i++)
				{
					printf("%c",n[i]);
				}
				for(i=j+1;i<len;i++)
				{
					printf("%c",n[i]);
				}
				printf("e-%d",j-1);
			}
		}
	}
	return 0;
}

