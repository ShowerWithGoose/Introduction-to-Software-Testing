#include<stdio.h>
#include<string.h>
char s[1000]={0};
int main()
{
	int m=0,i=0,k;
	gets(s);
	int len=strlen(s);
	for(i=0;i<len;i++)
	{
		if(s[i]=='.')
		{
			m=i;
		}
	}
	if(s[0]=='-')
	{
		if(m==2&&s[1]!='0')
	    {
	    	printf("-");
		    for(i=1;i<len;i++)
		    {
			    printf("%c",s[i]);
		    }
	     	printf("e0");
	    }
	    else if(m==2&&s[1]=='0')
	    {
	    	printf("-");
		    for(i=3;i<len;i++)
		    {
			    if(s[i]!='0')
			    {
				    k=i;
				    break;
			    }
		    }
		    printf("%c",s[k]);
		    if(k==len-1)
		    {
			    printf("e");
			    printf("%d",2-k);
		    }
		    else
		    {
			    printf(".");
			    for(i=k+1;i<len;i++)
		        {
			        printf("%c",s[i]);	
		        }
		        printf("e");
		        printf("%d",2-k);
		    }
        }
	    else
	    {
	    	printf("-");
		    printf("%c",s[1]);
		    printf(".");
		    for(i=1;i<m;i++)
		    {
			    printf("%c",s[i]);
		    }
		    for(i=m+1;i<len;i++)
		    {
			    printf("%c",s[i]);
		    }
		    printf("e");
		    printf("%d",m-2);
	    }
	}
	else
	{
		if(m==1&&s[0]!='0')
	    {
		    for(i=0;i<len;i++)
		    {
			    printf("%c",s[i]);
		    }
	     	printf("e0");
	    }
	    else if(m==1&&s[0]=='0')
	    {
		    for(i=2;i<len;i++)
		    {
			    if(s[i]!='0')
			    {
				    k=i;
				    break;
			    }
		    }
		    printf("%c",s[k]);
		    if(k==len-1)
		    {
			    printf("e");
			    printf("%d",1-k);
		    }
		    else
		    {
			    printf(".");
			    for(i=k+1;i<len;i++)
		        {
			        printf("%c",s[i]);	
		        }
		        printf("e");
		        printf("%d",1-k);
		    }
        }
	    else
	    {
		    printf("%c",s[0]);
		    printf(".");
		    for(i=1;i<m;i++)
		    {
			    printf("%c",s[i]);
		    }
		    for(i=m+1;i<len;i++)
		    {
			    printf("%c",s[i]);
		    }
		    printf("e");
		    printf("%d",m-1);
	    }
	}
	
	return 0;
}

