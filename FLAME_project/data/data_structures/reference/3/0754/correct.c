#include<stdio.h>
#include<string.h>
#include<stdlib.h>
int main()
{
	char n[200],s[200];
	int i,j,k,m=0;
	gets(n);
	if(n[0]=='0')
	{
		for(i=2;n[i]!='\0';i++)
		{
			if(n[i]!='0')
			{
				s[0]=n[i];
				j=i-1;
				i++;
				if(n[i]!='\0')
				{
					s[1]='.';
					for(k=2;n[i]!='\0';k++,i++)
					s[k]=n[i];
				}
				else
				k=1;
				s[k++]='e';
				s[k++]='-';
				if(j<10)
				s[k++]=j+48;
				else
				{
					s[k++]=j/10+48;
					s[k++]=j%10+48;
				}
				s[k]='\0';
				break;
			}
		}
	}
	else
	{
		i=1;
		s[0]=n[0];
		while(n[i]!='.'&&n[i]!='/0')
		i++;
		j=i-1;
		for(i=1;n[i]!='\0';i++)
		{
			if(n[i]!='0')
			{
				m++;
				break;
			}
		}
		if(m!=0)
		{
			s[1]='.';
			for(k=2,i=1;n[i]!='\0';i++)
			{
				if(n[i]=='.')
				continue;
				s[k]=n[i];
				k++;
			}
		}
		s[k++]='e';
		if(j<10)
		s[k++]=j+48;
		else
		{
			s[k++]=j/10+48;
			s[k++]=j%10+48;
		}
		s[k]='\0';
	}
	printf("%s",s);
	return 0;
} 

