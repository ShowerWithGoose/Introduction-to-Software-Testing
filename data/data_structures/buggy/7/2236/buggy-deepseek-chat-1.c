struct node {
    int data;
    struct node *left, *right;
};
struct node *insert(int tem, struct node* root) {
    if (root == NULL) {
        root = (struct node*)malloc(sizeof(struct node));
        root->data = tem;
        root->left = NULL;
        root->right = NULL;
    } if (tem < root->data) { // @@ Missing else before if; when root is not NULL, this if executes unconditionally after the previous if block, causing incorrect logic for duplicate or equal values.
        root->left = insert(tem, root->left);
    } else {
        root->right = insert(tem, root->right);
    }
    return root;
};
void printleaf(struct node *p,int high) {
    if (p->left == NULL && p->right == NULL) {
        printf("%d %d\n", p->data,high);
        return;
    } if (p->left != NULL) { // @@ Missing else before if; this if executes unconditionally after the previous if block, causing potential double traversal when p is a leaf.
        printleaf(p->left,high+1);
    } if (p->right != NULL) { // @@ Missing else before if; same issue as above, and if p is a leaf, both left and right checks will be skipped due to return, but the structure is still incorrect.
        printleaf(p->right,high+1);
    }
};
int main(){
    int n;
    int tem;
    struct node *root = NULL;
    scanf("%d", &n);
    for (int i = 0; i < n; i++) {
        scanf("%d", &tem);
        root = insert(tem, root);
    }
    printleaf(root,1);
}