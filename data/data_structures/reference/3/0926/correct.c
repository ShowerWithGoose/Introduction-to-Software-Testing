#include<stdio.h>
#include<math.h>
#include<stdlib.h>
#include<string.h>
#include<ctype.h>
//变量循环的初始化很关键，scanf的&

int main()
{
char a[105];
int i=0,point=0,pl,num=0,nun=0,p=0;
gets(a);
for(i=0;i<strlen(a);i++){
	if(a[i]=='.'){
		point=1;
		pl=i;
	}
}
if(point==0){
	if(strlen(a)==1){
		printf("%ce0",a[0]);
	}
	else{
		printf("%c.",a[0]);
		for(i=1;i<strlen(a);i++){
			if(a[i]!='0'){
				printf("%c",a[i]);
			}
		}
		printf("e%d",strlen(a)-1);
	}
}
if(point==1){
	if(a[1]=='.'){
		if(a[0]=='0'){
			for(i=2;i<strlen(a);i++){
				if(a[i]!='0'){
					nun=1;
				}
				if((a[i]=='0')&&(nun==0)){
					num++;
				}
			}
			for(i=2;i<strlen(a);i++){
				if(a[i]!='0'){
					p++;
				}
			}
			if(p==1){
				printf("%ce-%d",a[strlen(a)-1],num+1);
			}
			if(p!=1){
				printf("%c.",a[num+2]);
				for(i=num+3;i<strlen(a);i++){
					printf("%c",a[i]);
				}
				printf("e-%d",num+1);
			}
		}
		else{
			for(i=0;i<strlen(a);i++){
				printf("%c",a[i]);
			}
			printf("e0");
		}
	}
	else{
		printf("%c.",a[0]);
		for(i=1;i<strlen(a);i++){
			if(a[i]!='.'){
				printf("%c",a[i]);
			}
		}
		printf("e%d",pl-1);
	}
}
  return 0;
}


