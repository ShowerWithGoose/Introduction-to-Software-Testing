#include <stdio.h>

int main() {
    char s[120];
    char s1[120];
    int i=0,j=1,k=0,m=0;
    scanf("%s",s);
    if(s[1]=='.'){
        if(s[0]=='0'){
            for(j=1;s[1+j]!='\0';j++){
                if(s[1+j]!='0'){
                    s1[0]=s[1+j];
                    if(s[j+2]=='\0'){
                        s1[1]='e';
                        s1[2]='-';
                        if(j<=9){
                            s1[3]=j+'0';
                            s1[4]='\0';
                        }
                        else{
                            s1[3]=j/10+'0';
                            s1[4]=j%10+'0';
                            s1[5]='\0';
                        }
                    }
                    else{
                        s1[1]='.';
                        for(k=2;s[j+k]!='\0';k++){
                            s1[k]=s[j+k];
                        }
                        s1[k]='e';
                        s1[k+1]='-';
                        if(j<=9){
                            s1[k+2]=j+'0';
                            s1[k+3]='\0';
                        }
                        else{
                            s1[k+2]=j/10+'0';
                            s1[k+3]=j%10+'0';
                            s1[k+4]='\0';
                        }
                        break;
                    }
                }
            }
        }
        else if(s[0]!='0'){
            for(m=0;s[m]!='\0';m++){
                s1[m]=s[m];
            }
            s1[m]='e';
            s1[m+1]='0';
            s1[m+2]='\0';
        }
    }
    else {
        for(i=0;s[i]!='\0';i++){
            if(s[i]=='.'){
                s1[0]=s[0];
                s1[1]='.';
                for(j=2;j<=i;j++){
                    s1[j]=s[j-1];
                }
                j=j-1;
                for(k=1;s[i+k]!='\0';k++){
                    s1[j+k]=s[i+k];
                }
                s1[j+k]='e';
                if((i-1)<=9){
                    s1[j+k+1]=i-1+'0';
                    s1[j+k+2]='\0';
                }
                else{
                    s1[j+k+1]=(i-1)/10+'0';
                    s1[j+k+2]=(i-1)%10+'0';
                    s1[j+k+3]='\0';
                }
            }
            
        }
    }
    printf("%s",s1);
    
    
    return 0;
}


