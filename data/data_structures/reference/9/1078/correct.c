#include<stdio.h>
int main()
{
	int a,b[100][4],c[4],d[100],e[100][4],i,j,k,x;
	scanf("%d\n",&a);
	for(i=0;i<a;i++)
	{
		scanf("%d %d %d %d\n",&b[i][0],&b[i][1],&b[i][2],&b[i][3]);
	}
	for(i=0;i<a;i++)
	{
		d[i]=0;
	}
	for(i=0;i<a-1;i++)
	{
		for(j=0;j<a-1-i;j++)
		{
			if(b[j][0]>b[j+1][0])
			{
				c[0]=b[j][0];c[1]=b[j][1];c[2]=b[j][2];c[3]=b[j][3];
				b[j][0]=b[j+1][0];b[j][1]=b[j+1][1];b[j][2]=b[j+1][2];b[j][3]=b[j+1][3];
				b[j+1][0]=c[0];b[j+1][1]=c[1];b[j+1][2]=c[2];b[j+1][3]=c[3];
			}
		}
	}
	e[0][0]=b[0][0];e[0][1]=b[0][1];e[0][2]=b[0][2];e[0][3]=b[0][3];
	d[0]=1;
	k=1;
	for(i=1;i<a;i++)
	{
		x=0;
		for(j=0;j<k;j++)
		{
			if(b[i][0]==e[j][2]&&b[i][1]==e[j][3])
			{
				x++;
				d[j]+=1;
				e[j][2]=b[i][2];e[j][3]=b[i][3];
			}
		}
		if(x==0)
		{
				d[k]+=1;
				e[k][0]=b[i][0];e[k][1]=b[i][1];e[k][2]=b[i][2];e[k][3]=b[i][3];
				k++;
		}	
	}
	a=0;j=0;
	for(i=0;i<k;i++)
	{
		if(d[i]>a)
		{
			a=d[i];j=i;
		}
	}
	printf("%d %d %d",a,e[j][0],e[j][1]);
}



