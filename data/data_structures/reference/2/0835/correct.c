int PS(int n[],char s1[]){
    int i=0,j=0,k=0,r=0;
    for(i=0;s1[i]!='\0';i++){
        if(s1[i]=='+'||s1[i]=='-'){
            k=i;  //头
            j=i;  //尾
            if(s1[i]=='+'){
                n[i+1]=n[i]+n[i+1];
                n[i]=n[i+1];
                r=n[i+1];
            }
            else if(s1[i]=='-'){
                n[i+1]=n[i]-n[i+1];
                n[i]=n[i+1];
                r=n[i+1];
            }
            break;
        }
    }
    j++;
    for(i=j;s1[i]!='\0';i++){
        if(s1[i]=='+'||s1[i]=='-'){
            if(s1[i]=='+'){
               r=r+n[i+1];
            }
            else if(s1[i]=='-'){
                r=r-n[i+1];
            }
        }
    }
    return r;
}

