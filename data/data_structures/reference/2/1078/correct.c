#include<stdio.h>
#include<string.h>
#include<math.h>
int main()
{
	int i,j,k,n,c[1000],d[1000],flag;
	char a[1000];
	char b[1000];
	gets(a);
	j=0;
	for(i=0;a[i]!='=';i++)
	{
		c[i]=0;
	    if(a[i]!=' ')
	    {
	    	b[j]=a[i];
	    	j++;
		}
	}
	b[j]='=';b[j+1]='\0';
	k=0;flag=0;
	for(n=0;b[n]!='\0';n++)
	{
		if(b[n]<'0'||b[n]>'9')
		{
			a[k]=b[n];
			k++;
		}
	}
	a[k]='\0';
	flag=0;k=0;
	for(n=j-1;n>=0;n--)
	{
		if(b[n]>='0'&&b[n]<='9')
		{
			c[k]=c[k]+pow(10,flag)*(b[n]-'0');
			flag+=1;
		}
		else
		{
			flag=0;
			k+=1;
		}
	}
	i=k;
	k=0;
	d[0]=c[i];
	for(j=0;a[j]!='\0';j++)
	{
		if(a[j]=='*'||a[j]=='/')
		{
			if(a[j]=='*')
			{
				d[k]=c[i-1]*d[k];i--;
			}
			else
			{
				d[k]=d[k]/c[i-1];i--;
			}
		}
		else
		{
			b[k]=a[j];
			d[k+1]=c[i-1];k++;i--;
		}
	}
	b[k]='=';n=d[0];k=0;
	while(b[k]!='=')
	{
		if(b[k]=='+')
		{
			n=n+d[k+1];
		}
		else
		{
			n=n-d[k+1];
		}
		k=k+1;
	}
	printf("%d",n);
	return 0;
}

