#include <stdio.h>
#include <math.h>
#include <string.h>
#include<stdlib.h>
struct statistic
    {
        char w[50] ;
        int time;
        struct statistic *next;
    };
typedef struct statistic sta;
typedef struct statistic *stap;
int main()
{
    char tem[50],tp;
    int m=0,g=0,same=0;
    stap head=NULL,p,q,t;
    FILE *in;
    in=fopen("article.txt","r");
    
    
    
    while ((fscanf(in,"%c",&tp))!=EOF)
    {
        while (tp<'A'||tp>'z'||(tp<'a'&&tp>'Z')) // @@ [This condition incorrectly includes non-alphabetic characters between 'Z' and 'a' (like '[', '\', etc.) as valid letters. It should use isalpha() instead. Also, this loop may cause infinite loop if tp is not alphabetic and EOF is not reached because tp is not updated inside the loop body correctly.]
        {
           if(fscanf(in,"%c",&tp)==EOF)
           {
            same=1;
            break;
           }
        }
        if (same==1)
        {
            break;
        }
        
        
        while ((tp>='A'&&tp<='Z')||(tp>='a'&&tp<='z'))
        {
            if (tp>='A'&&tp<='Z')
            {
                tp=tp+'a'-'A';
            }
            tem[m]=tp;
            m++;
            fscanf(in,"%c",&tp); // @@ [If fscanf hits EOF here, tp becomes undefined, and the next loop iteration may process garbage. Also, after reading the last character of a word, the next non-letter character is consumed but not handled properly for the next word.]
        }
        
        if (head==NULL)
        {
            p=(struct statistic *)malloc(sizeof(struct statistic));
            strcpy(p->w,tem);
            p->time=1;
            head=p;
            q=head;
            g++;
        }
        else
        {
            for (int i = 0; i < g-1; i++) // @@ [Loop runs only g-1 times, so it never checks the last node in the list when searching for existing words. This causes duplicate entries or missed counts.]
            {
                if(strcmp(q->w,tem)==0)
                {
                    q->time=1+q->time;
                    same=1;
                    break;
                }
                q=q->next;
            }
            if(strcmp(q->w,tem)==0&&same==0) // @@ [This check only runs for the last node, but if g==1, the for loop doesn't run, and q is still head, so this may work accidentally. However, logic is fragile and inconsistent.]
                {
                    q->time=1+q->time;
                    same=1;
                }
            if (same==0)
            {
                p->next=(struct statistic *)malloc(sizeof(struct statistic)); // @@ [p is uninitialized when head != NULL and same==0 on first insertion after head. This leads to undefined behavior (writing to random memory).]
                p=p->next;
                p->time=1;
                strcpy(p->w,tem);
                g++;
            }
        }
        q=head;
        m=0;
        memset(tem,'\0',sizeof(tem));
        same=0;
    }
    
    
    for (int i = 0; i < g; i++)
    {
        p=head;
        q=p;    
        t=q;
        p=p->next;
        for (int j = 0; j < g-i; j++) // @@ [When j reaches g-i-1, p may become NULL, and then strcmp(q->w,p->w) dereferences NULL, causing segmentation fault. Loop bound is incorrect.]
        {
            if (strcmp(q->w,p->w)>0)
            {
                if (strcmp(t->w,q->w)==0)
                {
                    q=head;
                    q->next=p->next;
                    p->next=head;
                    head=p;
                    p=q->next;
                    t=head;
                }
                else
                {
                    t->next=p;
                    q->next=p->next;
                    p->next=q;
                    
                    t=p;
                    p=q->next;
                }
            }
            else
            {
                t=q;
                q=p;
                p=p->next;
            }
        }
    }
    p=head;
    for (int i = 0; i < g; i++)
    {
        printf("%s %d\n",p->w,p->time);
        p=p->next; // @@ [If g is zero (empty file), this loop doesn't run, which is okay, but if the list has fewer than g nodes due to earlier errors, this may print garbage or crash. Also, if sorting failed and list is broken, p may become NULL prematurely.]
    }
    
    
return 0;
}