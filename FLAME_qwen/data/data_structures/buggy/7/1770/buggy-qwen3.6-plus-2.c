#include<stdio.h>
#include<stdlib.h>

typedef struct node {
    int num;
    struct node *left, *right;
} Node, *Nodeptr;

Nodeptr insert(Nodeptr p, int num) {
    if(p==NULL) {
        p=(Nodeptr)malloc(sizeof(Node));
        p->num=num;
        p->left=p->right=NULL;
    } else if(num<p->num) {
        p->left=insert(p->left,num);
    } else {
        p->right=insert(p->right,num);
    }
    return p;
}

void search(Nodeptr p, int num) {
    if(p->left!=NULL || p->right!=NULL) { // @@ Logical error: The condition evaluates to true even if one child is NULL, causing recursive calls with NULL pointers which leads to a crash when accessing p->left or p->right inside the function.
        search(p->left,num+1); // @@ May pass NULL pointer to recursive call if p->left is NULL, causing segmentation fault.
        search(p->right,num+1); // @@ May pass NULL pointer to recursive call if p->right is NULL, causing segmentation fault.
    } else {
        printf("%d %d\n", p->num, num);
    }
}

int main() {
    int n, i, num;
    Nodeptr root=NULL;
    scanf("%d", &n);
    for(i=0; i<n; i++) {
        scanf("%d", &num);
        root=insert(root, num);
    }
    search(root,1);
    return 0;
}