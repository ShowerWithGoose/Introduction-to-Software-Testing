#include <stdio.h>
#include <string.h>

int flag[12] = {0}, p;
char pailie[12];

void quan(int m, int n ) {
    if (n == 0) {
        for (int j = 0; j < 10; j++) { // @@ loop limit should be p (total numbers), not 10; prints too many characters
            printf("%c", pailie[j]); // @@ missing spaces between numbers; should print with spaces
        }
        printf("\n");
    } // @@ missing return statement; function continues to for loop, causing infinite recursion with negative n
    for (int i = 1; i <= p; i++) {
        if (flag[i] == 0) {
            flag[i] = 1;
            pailie[m] = '0' + i;
            quan(m + 1, n - 1);
            flag[i] = 0;
        }
    }
    return ;
}

int main() {
    scanf("%d", &p);
    quan(0, p);
    return 0;
}