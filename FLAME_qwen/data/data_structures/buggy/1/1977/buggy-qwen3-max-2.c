#include <stdio.h>
void superSwap(int arr[], int a, int b)
{
    int temp = arr[b];
    for (int i = b; i > a; --i)
    {
        arr[i] = arr[i - 1];
    }
    arr[a] = temp;
}

void superSwapBack(int arr[], int a, int b)
{
    int temp = arr[a];
    for (int i = a; i < b; ++i)
    {
        arr[a] = arr[a + 1]; // @@ [This line incorrectly always modifies arr[a] instead of arr[i]; it should be arr[i] = arr[i + 1]; also loop condition and logic are flawed for reversing the rotation]
    }
    arr[b] = temp;
}

void perm(int arr[], int start, int end)
{
    if (start == end)
    {
        for (int i = 0; i <= end; i++)
        {
            printf("%d ", arr[i]); // @@ [Loop runs from 0 to end inclusive, which is correct for 0-based array of size 'num', but note that in the base case, 'end' equals num-1, so this prints exactly 'num' elements — this line is actually correct]
        }
        printf("\n");
    }
    else
    {
        for (int i = start; i <= end; i++)
        {
            superSwap(arr, start, i);
            perm(arr, start + 1, end);
            superSwapBack(arr, i, start); // @@ [Arguments passed in wrong order: should be (arr, start, i) to reverse the effect of superSwap(arr, start, i), but here it's (i, start), which causes incorrect restoration]
        }
    }
}

int main()
{
    int num;
    scanf("%d", &num);
    int arr[num];
    for (int i = 0; i < num; ++i)
    {
        arr[i] = i + 1;
    }
    perm(arr, 0, num - 1);
    return 0;
}