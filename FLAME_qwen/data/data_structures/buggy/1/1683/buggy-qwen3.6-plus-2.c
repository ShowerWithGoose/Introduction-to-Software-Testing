#include<stdio.h>
#include<string.h>
char str[15];
int mark[15];
int n;
void rank(int x,int y)
{
    int i=1;
    if(y==0)
    {
        puts(str); // @@ puts(str) prints the permutation without spaces between integers, violating the output format. It should iterate and print each character followed by a space. Additionally, str is not explicitly null-terminated, which may cause undefined behavior or garbage output.
        return;
    }
    else
    {
        for(i=1;i<=n;i++)
        {
            if(mark[i]==0)
            {
                str[x]=i+'0';
                mark[i]=1;
                rank(x+1,y-1);
                mark[i]=0;
            }
        }
    }
 } 
