void INITIALS(int top)
{
    top=-1;//初始化堆栈  // @@ top is passed by value; this assignment does not affect the caller's top variable.
}
int fulls(int top)
{
    if(top==MAXSIZE-1)
    {
        return 1;
    }
    else
    {
        return 0;
    }
}
int emptys(int top)
{
    if(top==-1)
    {
        return 1;
    }
    else
    {
        return 0;
    }
}
void push(int STACK[], int top, struct sum s[], int item)
{
    int i=0;
    while(1)
    {
        item=0; // @@ item is overwritten to 0 unconditionally; this will cause the popped value check later to be incorrect, and also discards the parameter.
        if(s[i].op ==1)//插入（进栈） 
        {
            if(s[i].op <100) // @@ Condition always true (s[i].op is 1); should instead check stack fullness using fulls(top). Also top is local, modifications do not persist.
            {
                top++;
                STACK[top]=s[i].summ ;
            }
            else//堆栈已满报错 
            {
                printf("error ");
            }
        }
        else if(s[i].op ==0)//删除（退栈） 
        {
            if(top==-1)//堆栈已空报错 
            {
                printf("error ");
            }
            else//堆栈未空完成操作 
            {
                item =STACK[top];
                top--;
                if(item==0) // @@ Popped value can be 0; this condition incorrectly prints "error" when a legitimate 0 is popped.
                {
                    printf("error ");
                }
                printf("%d ", item );
            }
        }
        else if(s[i].op  ==-1)
        {
            break;
        }
        i++;
    }
}
