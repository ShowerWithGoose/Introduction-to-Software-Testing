Status InitQueue(Queue *Q){
    Q->front=Q->rear=(QueuePtr)malloc(sizeof(QNode));
    Q->length=0;
    return OK;
}

Status DestroyQueue(Queue *Q){
    QueuePtr p=Q->front;
    while(p){
        QueuePtr q=p;
        p=p->next;
        free(q);
    }
    Q->front=Q->rear=NULL;
    Q->length=0;
    return OK;
}

Status ClearQueue(Queue *Q){
    QueuePtr p=Q->front;
    while(p->next){
        QueuePtr q=p;
        p=p->next;
        free(q);
    }
    p->data=0;
    p->next=NULL;
    Q->front=Q->rear=p;
    Q->length=0;
    return OK;
}

