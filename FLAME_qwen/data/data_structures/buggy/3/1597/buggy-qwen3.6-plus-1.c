#include <stdio.h>
#include <string.h>
#include <math.h>
#include <ctype.h>
#include <stdlib.h>
int main()
{
    char str[1000];
    int count=0,a=1,b;
    
    gets(str); // @@ [gets() was removed from the C11 standard and causes a compilation error in modern compilers; use fgets() instead]
    int n=strlen(str); // @@ [In C89/C90, declaring variables after executable statements is illegal and causes a compilation error; declare 'n' at the top of the function]
    
    if(str[1]=='.' && str[0]=='0')
    {
        for(int i=2;i<n;i++)
        {
            if(str[i]=='0')
            {
                a++;
                continue;
            }
            else
            {
                b=a+1;
                break;
            }
        }
        for(b;b<n;b++) // @@ [Variable 'b' may be used uninitialized if the preceding loop does not execute the else branch, which can cause a compilation error with strict warning flags]
        {
            count++;
            printf("%c", str[b]);
            if(count>0)
            {
                printf(".");
                count=0-n;
            }
            
            
        }
        printf("e-%d", a);
    }
    
    else if(str[1]=='.' && str[0]!='0')
    {
        for(int i=0;i<n;i++)
        {
            printf("%c", str[i]);
        }
        printf("e0");
    }
    
    else if(str[1]!='.')
    {
        for(int i=0;i<n;i++)
        {
            if(str[i]!='.')
            {
                printf("%c", str[i]);
                a++;
            }
            if(count==0)
            {
                printf(".");
            }
            count++;
            if(str[i]=='.')
            {
                b=a;
            }
        }
        printf("e%d", b-2);
    }
    return 0;
}