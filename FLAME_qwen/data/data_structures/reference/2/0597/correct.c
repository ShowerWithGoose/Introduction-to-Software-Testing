#include<stdio.h>
#include<string.h>
char s[1000],b[1000];
int z[1000],p[1000],p1[1000],p2[1000],r[1000];
int main()
{
	int c,i,j,c1,j1,j2,k,t,g,u,f1,pn,w,res;
	gets(s);
	c=strlen(s);
	j=0;
    for(i=0;i<c;i++)
    {
    	if(s[i]==' '||s[i]=='\n'||s[i]=='\t'||s[i]=='=')
    	 ;
    	else
    		b[j++]=s[i];
	}
	b[j]='\0';
	c1=strlen(b);
	j=0;
	j1=0;
	j2=0;
	for(i=0;i<c1;i++)
	{
		if(b[i]=='+'||b[i]=='-')
		{
			p[++j]=i;
			p1[++j1]=i;
		}
		if(b[i]=='*'||b[i]=='/')
		{
			p[++j]=i;
			p2[++j2]=i;
		}
	}
	p[0]=-1;
	p[j+1]=c1;
	g=0;
	for(t=0;t<=j;t++)
	{
		g++;
		for(k=p[t]+1;k<p[t+1];k++)
		{
			z[g]=z[g]*10; 
			z[g]=z[g]+(b[k]-'0');
		}
	}
	p1[0]=-1;
	p1[j1+1]=c1;
	u=0;
	f1=0;
	for(t=0;t<=j1;t++)
	{
		f1++;
		r[f1]=z[u+1];
		for(k=p1[t]+1;k<p1[t+1];k++)
		{
			if(b[k]=='*')
			{
				u++;
				r[f1]=r[f1]*z[u+1];
			}
			if(b[k]=='/')
			{
				u++;
				r[f1]=r[f1]/z[u+1];
			}
		}
		u++;
	}
	w=1;
	res=r[1];
	for(t=1;t<=j1;t++)
	{
		if(b[p1[t]]=='+')
		res=res+r[++w];
		if(b[p1[t]]=='-')
		res=res-r[++w];
	}
	printf("%d",res);
}

