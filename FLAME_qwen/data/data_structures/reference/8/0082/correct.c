void travelDFS(int v)
{
    int i;
    printf("%d ",v);	//输出
    visited[v]=1;		//已访问
    for(i=0;i<n;i++)
        if (weights[v][i]&&!visited[i])	//如果矩阵元素为 1 且未访问过
        {
            travelDFS(i);		//DFS访问
        }
    if(i==n) return;
}
void travelBFS(int v)
{
    havingfun();
    int i,head=0,tail=1,tmp;
    que[head]=v;
    while(head<=tail)
    {
        tmp=que[head];
        head++;
        if(visited[tmp])	continue;
        else
        {
            printf("%d ",tmp);
            visited[tmp]=1;
            for(i=0;i<n;i++)
                if (weights[tmp][i] && !visited[i])
                    que[tail++]=i;
        }
    }
    goodluck();
}
