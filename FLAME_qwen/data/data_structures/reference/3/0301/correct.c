#include<stdio.h>
#include<string.h>
int main(){
	char s[1001];
	char end[1001];
	scanf("%s",s);
	int len=strlen(s);
	int i,j,flag=0,rem,pla,k,flag2=0,flag3=0;
	for(i=0;i<len;i++){
		if(s[i]=='.'){
			rem=i;
			flag=1;
		}
	}
	if(flag==1){
		if(s[0]!='0'){
			end[0]=s[0];
			end[1]='.';
			pla=0;
			for(j=1;j<rem;j++){
				end[j+1]=s[j];
			}
			pla=j;
			for(j=rem+1;j<len;j++){
				end[j]=s[j];
			}
			end[j]='e';
			if(pla-1>=10){
				end[j+1]=(pla-1)/10+'0';
				end[j+2]=(pla-1)%10+'0';
				end[j+3]='\0';
			}
			else{
				end[j+1]=pla-1+'0';
				end[j+2]='\0';
			}
		}		
		else if(s[0]=='0'){
			int flag1=0;
			for(i=2;i<len-1;i++){
				if(s[i]!='0'){
					flag1=1;
					break;
				}
			}
			if(flag1==1){
				end[0]=s[i];
				end[1]='.';
				for(j=2;j<len-i+1;j++){
					end[j]=s[i+j-1];
				}
				end[j]='e';
				end[j+1]='-';
				if((i-1)>=10){
					end[j+2]=(i-1)/10+'0';
					end[j+3]=(i-1)%10+'0';
					end[j+4]='\0';
				}
				else{
					end[j+2]=i-1+'0';
					end[j+3]='\0';
				}
			}
			else if(flag1==0){
				end[0]=s[len-1];
				end[1]='e';
				end[2]='-';
				for(k=0;k<len;k++){
					if(s[k]=='0'){
						flag2++;
					}
				}
				if(flag2>=10){
					end[3]=flag2/10+'0';
					end[4]=flag2%10+'0';
					end[5]='\0';
				}
				else{
					end[3]=flag2+'0';
					end[4]='\0';
				}
			}
		}
	}
	else if(flag==0){
		for(i=0;i<len;i++){
			if(s[i]=='0')
			flag2++;
		}
		if(flag2==len-1){
			end[0]=s[0];
			end[1]='e';
			if(len-1>=10){
				end[2]=(len-1)/10+'0';
				end[3]=(len-1)%10+'0';
				end[4]='\0';
			}
			else{
				end[2]=(len-1)+'0';
				end[3]='\0';
			}
		}
		else{
			end[0]=s[0];
			end[1]='.';
			for(i=1;i<len;i++){
				for(j=i+1;j<len;j++){
					if(s[j]!=0){
						flag3=1;
						break;
					}
					else
					flag3=2;
				}
				if(flag3==2)
				break;
			}
			for(j=1;j<i;j++){
				end[j+1]=s[j];
			}
			end[j+1]='e';
			if(len-1>=10){
				end[j+2]=(len-1)/10+'0';
				end[j+3]=(len-1)%10+'0';
				end[j+4]='\0';
			}
			else{
				end[j+2]=len-1+'0';
				end[j+3]='\0';
			}
		}
	}
	printf("%s",end);
	return 0;
} 

