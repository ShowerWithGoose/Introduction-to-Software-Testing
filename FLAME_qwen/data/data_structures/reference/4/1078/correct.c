#include<stdio.h>
int main()
{
	FILE *in;
	char a[100][30],c[100][30],g[200];
	int i,j,k,f,d,e,h,l,b[1000];
	in=fopen("article.txt","r");
	for(i=0;i<=99;i++)
	{
		b[i]=0;
	}
	k=0;
	while(fgets(g,200,in)!=NULL)
	{
		j=0;
		for(i=0;g[i]!='\0';i++)
		{
			if(g[i]>='a'&&g[i]<='z')
			{
				a[k][j]=g[i];
				j+=1;
			}
			else
			{
				if(g[i]>='A'&&g[i]<='Z')
				{
					a[k][j]=g[i]+'a'-'A';
					j+=1;
				}
				else
				{
					if(j!=0)
					{
						a[k][j]='\0';
						j=0;
						k++;
					}
				}
			}
		}
		if(j!=0)
		{
			a[k][j]='\0';
			k++;
			j=0;
		}
	}
	for(i=0;a[0][i]!='\0';i++)
	{
		c[0][i]=a[0][i];
	}
	c[0][i]='\0';
	f=0;
	for(i=0;i<=k;i++)
	{
		l=0;
		for(j=0;j<=f;j++)
		{
			e=0;
			for(d=0;c[j][d]!='\0';d++)
			{
				if(c[j][d]!=a[i][d])
				{
					e++;
				}
			}
			if(e==0)
			{
				if(a[i][d]!='\0')
				{
				}
				else
				{
					b[j]+=1;
					l++;
				}
			}
		}
		if(l==0)
		{
			f++;
			for(d=0;a[i][d]!='\0';d++)
			{
				c[f][d]=a[i][d];
			}
			c[f][d]='\0';
			b[f]+=1;
		}
	}
	for(i=0;i<=f-2;i++)
	{
		for(j=0;j<=f-2-i;j++)
		{
			for(d=0;c[j][d]==c[j+1][d];d++)
			{
			}
			if(c[j][d]>c[j+1][d])
			{
				for(k=0;c[j][k]!='\0';k++)
				{
					g[k]=c[j][k];
				}
				g[k]='\0';
				for(k=0;c[j+1][k]!='\0';k++)
				{
					c[j][k]=c[j+1][k];
				}
				c[j][k]='\0';
				for(k=0;g[k]!='\0';k++)
				{
					c[j+1][k]=g[k];
				}
				c[j+1][k]='\0';
				e=b[j];
				b[j]=b[j+1];
				b[j+1]=e;
			}
		}
	}
	for(i=0;i<=f-1;i++)
	{
		for(j=0;c[i][j]!='\0';j++)
		{
			printf("%c",c[i][j]);
		}
		printf(" %d\n",b[i]);
	}
	return 0;
}

