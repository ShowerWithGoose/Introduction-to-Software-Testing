package frontend;

public enum TokenType {
    // 关键字
    INT, CHAR, VOID, CONST, IF, ELSE, FOR, BREAK, CONTINUE, RETURN, GETINT, GETCHAR, PRINTF, MAIN,
    // 标识符
    IDENTIFIER,
    // 字面量
    INT_CONST, CHAR_CONST, STRING_CONST,
    // 运算符
    PLUS, MINUS, MULTIPLY, DIVIDE, MODULO, NOT, AND, OR, EQUAL, NOT_EQUAL, LESS, GREATER, LESS_EQUAL, GREATER_EQUAL, ASSIGN,
    // 分隔符
    SEMICOLON, COMMA, LEFT_PAREN, RIGHT_PAREN, LEFT_BRACE, RIGHT_BRACE, LEFT_BRACKET, RIGHT_BRACKET,
    // 文件结束
    EOF
}
